#!/bin/sh

##################################################
################### parameters ###################
##################################################

pcds_directory=${1:?'Must specify the directory of the pcds'}


echo "############################################################################################################################################################"
echo "##### Generating ply files from pcds in path: ${videos_directory}"
echo "############################################################################################################################################################\n"

cd ${pcds_directory}

for file in *.pcd
do
	file_name=${file%.*}
	pcl_pcd2ply ${file_name}.pcd ${file_name}.ply &
done


wait

echo "\n############################################################################################################################################################"
echo "##### Finished generating ply files for path ${videos_directory}"
echo "############################################################################################################################################################\n"
